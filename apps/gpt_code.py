import random

# Список загадок
riddles = [
    "Что можно увидеть с закрытыми глазами?",
    "Что можно сломать, но нельзя исправить?",
    "Что идет вверх, но никогда не спускается?"
]

# Функция для загадывания случайной загадки
def ask_riddle():
    # Выбираем случайную загадку из списка
    riddle = random.choice(riddles)
    print("Загадка: " + riddle)
    
    # Получаем ответ от пользователя
    answer = input("Введите ваш ответ: ")
    
    # Проверяем ответ
    if answer == "закрытые глаза":
        print("Правильно! Это можно увидеть с закрытыми глазами.")
    elif answer == "сломать":
        print("Правильно! Это можно сломать, но нельзя исправить.")
    elif answer == "возраст":
        print("Правильно! Это идет вверх, но никогда не спускается.")
    else:
        print("Неправильно. Попробуйте еще раз.")
    
# Запускаем функцию загадывания загадки
ask_riddle()
